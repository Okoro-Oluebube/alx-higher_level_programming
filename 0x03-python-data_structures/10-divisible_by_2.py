def divisible_by_2(my_list=[]):
    new_list = my_list[:]
    for i in my_list:
        if i % 2 == 0:
            print(new_list)
            return (new_list, True)
            print(new_list)
        else:
            print(new_list)
            return (new_list, False)
            print(new_list)
        print(new_list)
